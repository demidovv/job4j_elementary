package ru.job4j.profession;

public class Profession {
    String name;
    String surname;
    String education;
    int birthday;

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getEducation() {
        return education;
    }

    public int getBirthday() {
        return birthday;
    }
}
